//*****************************************************************************
//
// IncS_Write_Com
//
//*****************************************************************************
void IncS_Write_Com(unsigned char INIT_COM) {
	unsigned char TEMPCOM;
	unsigned char scnt;
	TEMPCOM = INIT_COM;
	IncS_nCS_H;
	IncS_nCS_L;
	IncS_SCLK_L;
	IncS_nDC_L;
	for (scnt = 0; scnt < 8; scnt++) {
		if (TEMPCOM & 0x80)
			IncS_SDA_H;
		else
			IncS_SDA_L;
		//delay42ns(10);
		IncS_SCLK_H;
		//delay42ns(10);
		IncS_SCLK_L;
		TEMPCOM = TEMPCOM << 1;
		//delay42ns(10);
	}
	IncS_nCS_H;
}

