/***************功能描述：显示6*8一组标准ASCII字符串	显示的坐标（x,y），y为页范围0～7****************/
void IncS_P6x8Str(u8 x, u8 y, u8 ch[]) {
	u8 c = 0, i = 0, j = 0;
	while (ch[j] != '\0') {
		c = ch[j] - 32;
		if (x > 126) {
			x = 0;
			y++;
		}				//边界限制  强制拉回
		IncS_Set_Pos(x, y);
		for (i = 0; i < 6; i++)
			IncS_Write_Data(F6x8[c][i]);
		x += 6;
		j++;
	}

}
/*******************功能描述：显示8*16一组标准ASCII字符串	 显示的坐标（x,y），y为页范围0～7****************/
void IncS_P8x16Str(u8 x, u8 y, u8 ch[]) {
	u8 c = 0, i = 0, j = 0;
	//x=120-x;
	while (ch[j] != '\0') {
		c = ch[j] - 32;
		if (x > 120) {
			x = 0;
			y++;
		}					//边界限制  强制拉回

		for (i = 0; i < 8; i++) {					//下半部分
			IncS_Set_Pos(x, y + i);
			IncS_Write_Data(F8X16[c * 16 + i + 8]);
		}
		for (i = 0; i < 8; i++) {					//上半部分
			IncS_Set_Pos(x + 1, y + i);
			IncS_Write_Data(F8X16[c * 16 + i]);
		}

		y += 8;
		j++;
	}
}
/*****************功能描述：显示16*16点阵  显示的坐标（x,y），y为页范围0～7****************************/
void IncS_P16x16Ch(u8 x, u8 y, u8 num) {
	u8 wm = 0;
	u16 adder = 32 * num;
	IncS_Set_Pos(x, y);
	for (wm = 0; wm < 16; wm++) {
		IncS_Write_Data(F16x16[adder]);
		adder += 1;
	}
	IncS_Set_Pos(x, y + 1);
	for (wm = 0; wm < 16; wm++) {
		IncS_Write_Data(F16x16[adder]);
		adder += 1;
	}
}

